package operators;

public class RelationalOperator {

	public static void main(String[] args) {
		
		int a=50;
		int b=45;
		int c=50;
		
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a!=b);
		System.out.println(a==b);
		System.out.println(a<=c);
		
	}
}
